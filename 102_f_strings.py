usuario = "JoseCode"
intentos = 3

# ❌ MÉTODO ANTIGUO (Verboso y aburrido de escribir)
print("usuario:", usuario)
print("intentos:", intentos)



# ✅ MÉTODO PYTHONIC (Limpio, rápido y automático ✨)
usuario = "JoseCode"
intentos = 3

print(f"{usuario=}")
# Salida automática: usuario='JoseCode'

print(f"{intentos=}")
# Salida automática: intentos=3
